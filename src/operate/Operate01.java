package operate;

public class Operate01 {

	public static void main(String[] args) {
		// boolean 이용하기
		boolean power = false;
		System.out.println(power);
		
		power = !power; // false 값을 !를 이용해 뒤집는다
		System.out.println(power);
		
		power = !power; // 뒤집었던 값(참)을 또 뒤집는다
		System.out.println(power);
		

	}

}
